package com.Assignment3.Model;

public class QueryAndLink {
	String query;
	String link;

	public QueryAndLink(String query, String link) {
		super();
		this.query = query;
		this.link = link;
	}

	public String getQuery() {
		return query;
	}

	public void setQuery(String query) {
		this.query = query;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

}
